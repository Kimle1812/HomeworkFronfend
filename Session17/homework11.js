let number_a=Math.abs(-5.5);
let number_b=Math.abs(10);
console.log(number_a, number_b);
// hàm Math.abs giúp chuyển đổi các số âm thành số dương
let number_c=Math.ceil(4.4);
let number_d=Math.ceil(5.8);
console.log(number_c, number_d);
// hàm Math.ceil giúp làm tròn các số sau dấu phẩy lên số nguyên gần nhất
let number_e=Math.floor(5.7);
let number_f=Math.floor(6.1);
console.log(number_e, number_f);
// hàm Math.floor giúp làm tròn các số sau dấu phẩy xuống số nguyên gần nhất
let number_g=Math.round(10.1);
let number_h=Math.round(20.6);
console.log(number_g, number_h);
// hàm Math.round giúp làm tròn các số sau dấu phẩy đến số nguyên gần nhất